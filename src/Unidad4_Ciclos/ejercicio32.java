public class ejercicio32 {

    public static void main(String[] args) {
        // Iteramos sobre los números del 1 al 20
        for (int i = 1; i <= 20; i++) {
            // Verificamos si el número es par
            if (i % 2 == 0) {
                // Imprimimos el número par
                System.out.println(i);
            }
        }
    }
}
